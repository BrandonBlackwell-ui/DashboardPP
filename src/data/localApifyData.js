function totals(mentions) {
  return {
    posts: mentions.length,
    comments: mentions.reduce((sum, p) => sum + (p.comments || p.commentsExtracted || 0), 0),
    items: mentions.length,
    promedio_diario: mentions.length,
    likes: mentions.reduce((sum, p) => sum + (p.likes || 0), 0),
    reactions: mentions.reduce((sum, p) => sum + (p.reactions || 0), 0),
    views: mentions.reduce((sum, p) => sum + (p.views || 0), 0),
    retweets: mentions.reduce((sum, p) => sum + (p.retweets || 0), 0),
    quotes: mentions.reduce((sum, p) => sum + (p.quotes || 0), 0),
    shares: mentions.reduce((sum, p) => sum + (p.shares || 0), 0),
    bookmarks: mentions.reduce((sum, p) => sum + (p.bookmarks || 0), 0),
    plataforma_lider: mentions[0]?.platform || '',
    plataformas_activas: new Set(mentions.map(p => p.platform)).size,
  };
}

function mentionPost(p) {
  return {
    url: p.url || '',
    sourceUrl: p.sourceUrl || p.url || '',
    text: p.text,
    username: p.author,
    authorName: p.authorName || '',
    platform: p.platform,
    time: p.time,
    engagement: (p.reactions || p.likes || 0) + (p.retweets || 0) * 3 + (p.quotes || 0) * 2 + (p.comments || 0) * 2 + (p.shares || 0) * 3 + (p.bookmarks || 0),
    views: p.views || 0,
    reactions: p.reactions || 0,
    likes: p.likes || 0,
    comments: p.comments || 0,
    retweets: p.retweets || 0,
    quotes: p.quotes || 0,
    shares: p.shares || 0,
    bookmarks: p.bookmarks || 0,
    followers: p.followers || null,
    commentsExtracted: p.commentsExtracted || 0,
    commentsList: p.commentsList || [],
    thumbnail: p.thumbnail || '',
    urlVerified: !!p.urlVerified,
    type: p.type || 'post',
    contextAuthor: p.contextAuthor || '',
    contextText: p.contextText || '',
  };
}

